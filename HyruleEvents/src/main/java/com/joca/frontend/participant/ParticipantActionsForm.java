    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        ParticipantService participantService = new ParticipantService(new ParticipantDB());
        try {
            Optional<Filter> filter = getFilter();
            List<Participant> participants;
            if (filter.isEmpty()) {
                participants = participantService.getAllParticipants();
            } else {
                participants = participantService.getParticipantsByFilter(List.of(filter.get()));
            }
            GenericTable genericTable = new GenericTable(List.copyOf(participants), GenericTable.PARTICIPANT_MODEL, "Participantes encontrados");
            framePrincipal.openNewWindow(genericTable);
            framePrincipal.printOnLog( participants.size() + " Participantes encontrados.");
        } catch (InvalidRequisitesException | NotFoundException | SQLException e) {
            framePrincipal.printOnLog(e.getMessage());
        }
    }//GEN-LAST:event_searchButtonActionPerformed

