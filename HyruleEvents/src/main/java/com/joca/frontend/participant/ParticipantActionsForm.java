    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        ParticipantService participantService = new ParticipantService(new ParticipantDB());
        String email = searchBar.getText();
        try {
            int confirm = JOptionPane.showConfirmDialog(this,
                    "Eliminar participante con email " + email + " ?", "Confirmar eliminación",
                    JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                participantService.deleteParticipant(email);
                framePrincipal.printOnLog("Participante con email " + email + " eliminado con éxito");
                JOptionPane.showMessageDialog(this, "Participante con email " + email + " eliminado con éxito");
            }
        } catch (SQLException ex) {
            String message;
            if (ex.getErrorCode() == 1451) {
                message = "Error al intentar eliminar el participante: el participante se encuentra en uso en otros registros";
            } else {
                message = "Error al intentar eliminar el participante - codigo de error " + ex.getErrorCode();
            }
            JOptionPane.showMessageDialog(this, message);
            framePrincipal.printOnLog(message);
        } catch (NotRowsAffectedException ex) {
            framePrincipal.printOnLog("Error al intentar eliminar el participante - " + ex.getMessage());
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        ParticipantService participantService = new ParticipantService(new ParticipantDB());
        Participant participant;
        try {
            participant = participantService.getParticipantByID(searchBar.getText());
            ParticipantUpdateForm participantUpdateForm = new ParticipantUpdateForm(participant, participant.getEmail(), framePrincipal, UpdateTypeEnum.ACTUALIZAR);
            framePrincipal.openNewWindow(participantUpdateForm);
        } catch (NotFoundException | SQLException ex) {
            framePrincipal.printOnLog("Error al intentar actualizar el participante - " + ex.getMessage());
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void searchBarCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_searchBarCaretUpdate
        ParticipantService participantService = new ParticipantService(new ParticipantDB());
        try {
            boolean isParticipantAvailable = !participantService.isKeyInUse(searchBar.getText());
            if (isParticipantAvailable) {
                editButton.setEnabled(false);
                deleteButton.setEnabled(false);
            } else {
                editButton.setEnabled(true);
                deleteButton.setEnabled(true);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchBarCaretUpdate

    private Optional<Filter> getFilter() {
        if (searchBar.getText().isBlank()) {
            return Optional.empty();
        }
        return Optional.of(new Filter("email", searchBar.getText(), FilterTypeEnum.EQUAL));
    }

